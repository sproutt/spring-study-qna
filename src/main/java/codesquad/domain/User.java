package codesquad.domain;

import codesquad.exception.InvalidPasswordException;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;

@Entity
public class User {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;
    private String userId;
    private String password;
    private String name;
    private String email;

    public User(String userId, String password, String name, String email) {
        this.userId = userId;
        this.password = password;
        this.name = name;
        this.email = email;
    }

    public User() {

    }

    public void update(EditUserDto editUserDto) {
        if (equalsPassword(editUserDto.getPassword())) {
            this.password = editUserDto.getPassword();
            this.name = editUserDto.getName();
            this.email = editUserDto.getEmail();
        } else {
            throw new InvalidPasswordException();
        }
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public Long getId() {
        return id;
    }

    public String getUserId() {
        return userId;
    }

    public String getPassword() {
        return password;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public boolean equalsPassword(String passwd) {
        return this.password.equals(passwd);
    }
}
